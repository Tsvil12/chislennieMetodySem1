import numpy as np

# Функция
def f(x):
    return np.cos(np.sin(x)**2)

# Параметры
a = 0           # Левая граница интервала
b = np.pi       # Правая граница интервала
x0 = 1.5        # Точка, в которой ищем значение функции
epsilon = 1e-10 # Требуемая точность
n = 2           # Начальная степень полинома

# Шаги расчета:

# 1. Нахождение узлов Гаусса (для n=2)
t1 = -1 / np.sqrt(3)
t2 = 1 / np.sqrt(3)

# Приведение узлов к нашему интервалу [a, b]:
x1 = ((b - a) / 2) * t1 + (a + b) / 2
x2 = ((b - a) / 2) * t2 + (a + b) / 2

# 2. Значения функции в узлах:
y1 = f(x1)
y2 = f(x2)

# 3. Базисные полиномы Лагранжа:
def L1(x):
    return (x - x2) / (x1 - x2)

def L2(x):
    return (x - x1) / (x2 - x1)

# 4. Интерполяционный полином:
def P(x):
    return y1 * L1(x) + y2 * L2(x)

# 5. Расчет приближенного значения в точке x0:
p_x0 = P(x0)
true_value = f(x0)

# 6. Оценка погрешности:
error = abs(p_x0 - true_value)

# Результаты:
print("Исходные данные:")
print(f"Функция: f(x) = cos(sin²x)")
print(f"Интервал: [{a}, {b}]")
print(f"Начальная степень полинома: n = {n}")
print(f"Нужная точность: eps = {epsilon:.2e}")
print(f"Исследуемая точка: x0 = {x0}")

print("\nРасчётные данные:")
print(f"Узел x1 = {x1:.6f};   f(x1) = {y1:.6f}")
print(f"Узел x2 = {x2:.6f};   f(x2) = {y2:.6f}")

print("\nРезультат интерполяции:")
print(f"Приблизительное значение в точке x0: {p_x0:.6f}")
print(f"Точное значение в точке x0:          {true_value:.6f}")
print(f"Погрешность:                         {error:.6f}")

if error > epsilon:
    print("\nВнимание: Погрешность превышает заданную точность.")
else:
    print("\nТребуемая точность достигнута!")