# МЕТОД ГАУССА С ВЫБОРОМ ГЛАВНОГО ЭЛЕМЕНТА
# Методичка: стр. 35-37
# 
# Суть метода: привести матрицу к треугольному виду (прямой ход),
# а потом найти неизвестные (обратный ход).
# Выбор главного элемента нужен, чтобы не делить на ноль.
# ============================================================================

def gauss_solve(A, f):
    """
    Решение СЛАУ Ax = f методом Гаусса.
    
    ПРИМЕР:
    A = [[4, 1, 1],
         [1, 5, 1],
         [1, 1, 6]]
    f = [9, 14, 21]
    
    Ответ: x = [1, 2, 3]
    """
    n = len(A)
    
    # Копируем, чтобы не испортить исходные данные
    a = [row[:] for row in A]
    b = f[:]
    
    # ========== ПРЯМОЙ ХОД ==========
    for k in range(n):
        # --- ВЫБОР ГЛАВНОГО ЭЛЕМЕНТА ---
        # Ищем самую большую по модулю строку в k-м столбце
        max_row = k
        for i in range(k + 1, n):
            if abs(a[i][k]) > abs(a[max_row][k]):
                max_row = i
        
        # Меняем строки местами
        if max_row != k:
            a[k], a[max_row] = a[max_row], a[k]
            b[k], b[max_row] = b[max_row], b[k]
        
        # --- НОРМАЛИЗАЦИЯ (делим строку на диагональный элемент) ---
        diag = a[k][k]
        for j in range(k, n):
            a[k][j] = a[k][j] / diag
        b[k] = b[k] / diag
        
        # --- ИСКЛЮЧЕНИЕ (обнуляем всё ниже диагонали) ---
        for i in range(k + 1, n):
            factor = a[i][k]
            for j in range(k, n):
                a[i][j] = a[i][j] - factor * a[k][j]
            b[i] = b[i] - factor * b[k]
    
    # ========== ОБРАТНЫЙ ХОД ==========
    x = [0] * n
    for i in range(n - 1, -1, -1):
        x[i] = b[i]
        for j in range(i + 1, n):
            x[i] = x[i] - a[i][j] * x[j]
    
    return x


# ========== ПРИМЕР ИСПОЛЬЗОВАНИЯ ==========
if __name__ == "__main__":
    # Матрица 3x3 с известным решением x = [1, 2, 3]
    A = [
        [4, 1, 1],
        [1, 5, 1],
        [1, 1, 6]
    ]
    f = [9, 14, 21]  # 4*1 + 1*2 + 1*3 = 9, и т.д.
    
    x = gauss_solve(A, f)
    
    print("Решение методом Гаусса:")
    print(f"x = {x}")
    
    # Проверка: подставляем обратно
    print("\nПроверка A*x:")
    for i in range(len(A)):
        val = sum(A[i][j] * x[j] for j in range(len(A)))
        print(f"  Строка {i}: {val} (должно быть {f[i]})")